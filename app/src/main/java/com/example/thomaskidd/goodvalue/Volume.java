public class Volume {
    //All Measurements are given in equivalence to 1.0 litres

    //Volumetric Measurements
    private double litre = 1.0;
    private double millilitre = 0.001;
    private double gallon = 0.264172052;
    private double impGallon = 0.219969157;
    private double flOz = 33.814;
    private double impFlOz = 35.1951;
    /**
     * @return the litre
     */
    public double getLitre() {
        return litre;
    }
    /**
     * @return the millilitre
     */
    public double getMillilitre() {
        return millilitre;
    }
    /**
     * @return the gallon
     */
    public double getGallon() {
        return gallon;
    }
    /**
     * @return the impGallon
     */
    public double getImpGallon() {
        return impGallon;
    }
    /**
     * @return the flOz
     */
    public double getFlOz() {
        return flOz;
    }
    /**
     * @return the impFlOz
     */
    public double getImpFlOz() {
        return impFlOz;
    }
}
