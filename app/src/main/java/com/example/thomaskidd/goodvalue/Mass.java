public class Mass {
    //All Measurements are given in equivalence to 1.0 grams

    //Mass Measurements
    private double gram = 1.0;
    private double kilogram = 1000.0;
    private double ounce = 0.035274;
    private double pound = 0.00220462;
    /**
     * @return the gram
     */
    public double getGram() {
        return gram;
    }
    /**
     * @return the kilogram
     */
    public double getKilogram() {
        return kilogram;
    }
    /**
     * @return the ounce
     */
    public double getOunce() {
        return ounce;
    }
    /**
     * @return the pound
     */
    public double getPound() {
        return pound;
    }
}
